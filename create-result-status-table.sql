-- Create BMM_RESULT_STATUSES table
CREATE TABLE BMM_RESULT_STATUSES (
    ID VARCHAR2(36) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    DELETED_AT TIMESTAMP,
    CREATED_BY VARCHAR2(50),
    UPDATED_BY VARCHAR2(50),
    VERSION NUMBER NOT NULL,
    STATUS_CODE VARCHAR2(20) NOT NULL,
    STATUS_NAME VARCHAR2(200) NOT NULL,
    ORDER_NUMBER NUMBER NOT NULL,
    DESCRIPTION VARCHAR2(500),
    COLOR_CODE VARCHAR2(20),
    IS_ACTIVE NUMBER DEFAULT 1,
    
    CONSTRAINT PK_BMM_RESULT_STATUSES PRIMARY KEY (ID),
    CONSTRAINT UK_BMM_RS_CODE UNIQUE (STATUS_CODE)
);

-- Create Indexes for BMM_RESULT_STATUSES
CREATE INDEX IDX_BMM_RS_CODE ON BMM_RESULT_STATUSES(STATUS_CODE);
CREATE INDEX IDX_BMM_RS_ORDER ON BMM_RESULT_STATUSES(ORDER_NUMBER);
CREATE INDEX IDX_BMM_RS_ACTIVE ON BMM_RESULT_STATUSES(IS_ACTIVE);
CREATE INDEX IDX_BMM_RS_CREATED_AT ON BMM_RESULT_STATUSES(CREATED_AT);

