-- 6. Check foreign key constraints
SELECT CONSTRAINT_NAME, TABLE_NAME, COLUMN_NAME, R_CONSTRAINT_NAME
FROM USER_CONS_columns 
WHERE TABLE_NAME = 'BMM_RESULT_TRACKINGS' 
AND COLUMN_NAME IN ('REQUEST_ROOM_ID', 'IN_ROOM_ID', 'SAMPLE_TYPE_ID', 'RESULT_STATUS_ID', 'SERVICE_REQUEST_ID');

